# While - O loop `While` será realizado enquanto a condição  for verdadeira
x = 1
while x < 10:
    x += 1
    print(x)

# For
countdown = [4, 3, 2, 1, 0]
for number in countdown:
    print(number)

# for i in range(start, stop, step)
for i in range(5):
    print(i)

